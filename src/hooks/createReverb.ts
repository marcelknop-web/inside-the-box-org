/**
 * Shared impulse-response reverb generator for Web Audio hooks.
 * Used by useCisoSound and useMillionaireSound to avoid duplication.
 */
export function createReverb(ctx: AudioContext, duration = 1.5, decay = 2): ConvolverNode {
  const rate = ctx.sampleRate;
  const length = rate * duration;
  const impulse = ctx.createBuffer(2, length, rate);
  for (let ch = 0; ch < 2; ch++) {
    const data = impulse.getChannelData(ch);
    for (let i = 0; i < length; i++) {
      data[i] = (Math.random() * 2 - 1) * Math.pow(1 - i / length, decay);
    }
  }
  const convolver = ctx.createConvolver();
  convolver.buffer = impulse;
  return convolver;
}
